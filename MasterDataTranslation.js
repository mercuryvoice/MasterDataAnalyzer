/**
 * Gets the appropriate translation object based on the user's locale.
 * @returns {Object.<string, string>} An object containing all UI strings in the correct language.
 */
MasterData.getTranslations = function() {
    const locale = Session.getActiveUserLocale();
    // Fallback to 'en' if the user's locale is not 'zh_TW'
    const lang = (locale === 'zh_TW') ? 'zh_TW' : 'en';
    return MasterData.TRANSLATIONS[lang];
}
